alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z', 'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']
length = (len(alphabet)//2)

# ENCODING AND DECODING
def caesar(start_text, shift_amount, casear_direction):
  end_text = ""
  if casear_direction == "decode":
    shift_amount *= -1
  for char in start_text:
    if char in alphabet:
      position = alphabet.index(char)
      new_position = position + shift_amount
      end_text += alphabet[new_position]
    else:
      end_text += char
  print(f"Here's the {casear_direction}d result: {end_text}")

should_continue = True

while should_continue:
  direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
  text = input("Type your message:\n").lower()
  shift = int(input("Type the shift number:\n"))

  while shift > length:
    shift = shift % length
  caesar(start_text=text, shift_amount=shift, casear_direction=direction)
  result = input("type 'yes' if you want to continue otherwise type 'no' ")
  if result == 'no':
    should_continue = False
    print("Goodbye")